 * @package       app.Model
 */
class AppModel extends Model {

	public $actsAs = array('Utility.Enumerable', 'Containable');

/**
 * Verificar si el id que se proporciona es positivo
 *
 * @param  int  $check El id
 * @return boolean  true: es mayor a 0, false es menor o igual a 0
 */
	public function isPositive($check) {
		if ($check['id'] > 0) {
			return true;
		} else {
			return false;
		}
	}
}